# Part A - Spot the Bug
# -------------------------

def add_item(item, cart=[]):
    cart.append(item)
    return cart


print("Part A Output:")
print(add_item("apple"))
print(add_item("banana"))
print(add_item("milk", cart=["bread"]))
print(add_item("eggs"))

# Output:
# ['apple']
# ['apple', 'banana']
# ['bread', 'milk']
# ['apple', 'banana', 'eggs']
#
# Explanation:
# The default list (cart=[]) is created only once when the function is
# defined. Every call that doesn't provide a cart uses the same list.


# -------------------------
# Part B - Fix It
# -------------------------

def add_item_fixed(item, cart=None):
    if cart is None:
        cart = []
    cart.append(item)
    return cart


print("\nPart B Output:")
print(add_item_fixed("apple"))
print(add_item_fixed("banana"))
print(add_item_fixed("milk", ["bread"]))
print(add_item_fixed("eggs"))


