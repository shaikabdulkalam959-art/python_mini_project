# -------------------------

def create_cart(owner, discount=0):
    return {
        "owner": owner,
        "items": [],
        "discount": discount
    }


def add_to_cart(cart, name, price, qty=1):
    cart["items"].append({
        "name": name,
        "price": price,
        "qty": qty
    })


def update_price(price_tuple, new_price):
    try:
        # Tuples are immutable, so this raises TypeError
        price_tuple[1] = new_price
    except TypeError as e:
        print("\nTuple Error:", e)


def calculate_total(cart):
    total = 0

    for item in cart["items"]:
        total += item["price"] * item["qty"]

    total -= total * (cart["discount"] / 100)
    return total


# Create two separate carts
cart1 = create_cart("Alice", 10)
cart2 = create_cart("Bob")

# Add items to first cart
add_to_cart(cart1, "Laptop", 50000, 1)
add_to_cart(cart1, "Mouse", 1000, 2)

# Add items to second cart
add_to_cart(cart2, "Notebook", 50, 5)
add_to_cart(cart2, "Pen", 10, 10)

print("\nCustomer 1 Cart:")
print(cart1)
print("Total:", calculate_total(cart1))

print("\nCustomer 2 Cart:")
print(cart2)
print("Total:", calculate_total(cart2))

# Demonstrate tuple immutability
price = ("Laptop", 50000)
update_price(price, 55000)


